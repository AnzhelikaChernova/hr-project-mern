'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useAuthStore } from '@/lib/stores/auth';

const hrNavItems = [
  { href: '/dashboard', label: 'Dashboard', icon: DashboardIcon },
  { href: '/vacancies', label: 'Vacancies', icon: BriefcaseIcon },
  { href: '/applications', label: 'Applications', icon: DocumentIcon },
  { href: '/interviews', label: 'Interviews', icon: CalendarIcon },
  { href: '/profile', label: 'Profile', icon: UserIcon },
];

const candidateNavItems = [
  { href: '/dashboard', label: 'Dashboard', icon: DashboardIcon },
  { href: '/vacancies', label: 'Browse Jobs', icon: SearchIcon },
  { href: '/applications', label: 'My Applications', icon: DocumentIcon },
  { href: '/interviews', label: 'My Interviews', icon: CalendarIcon },
  { href: '/profile', label: 'Profile', icon: UserIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { user } = useAuthStore();

  const navItems = user?.role === 'HR' ? hrNavItems : candidateNavItems;

  return (
    <aside className="sidebar">
      <div className="sidebar-header">
        <Link href="/" className="flex items-center gap-3">
          <div className="w-10 h-10 rounded-xl bg-gradient-to-br from-blue-600 to-indigo-600 flex items-center justify-center">
            <svg className="w-6 h-6 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M21 13.255A23.931 23.931 0 0112 15c-3.183 0-6.22-.62-9-1.745M16 6V4a2 2 0 00-2-2h-4a2 2 0 00-2 2v2m4 6h.01M5 20h14a2 2 0 002-2V8a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z" />
            </svg>
          </div>
          <div>
            <span className="text-lg font-bold text-slate-900">HR Platform</span>
            <span className="block text-xs text-slate-500">Recruitment System</span>
          </div>
        </Link>
      </div>

      <nav className="sidebar-nav">
        <div className="mb-2 px-4">
          <span className="text-xs font-semibold text-slate-400 uppercase tracking-wider">Menu</span>
        </div>
        <ul className="space-y-1">
          {navItems.map((item) => {
            const isActive = pathname === item.href;
            const Icon = item.icon;
            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={`nav-item ${isActive ? 'nav-item-active' : ''}`}
                >
                  <Icon className="w-5 h-5" />
                  <span>{item.label}</span>
                  {isActive && (
                    <span className="ml-auto w-1.5 h-1.5 rounded-full bg-blue-600" />
                  )}
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      <div className="sidebar-footer">
        <div className="flex items-center gap-3 p-3 rounded-xl bg-slate-50">
          <div className="avatar avatar-md">
            {user?.firstName?.[0]}{user?.lastName?.[0]}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-semibold text-slate-900 truncate">{user?.fullName}</p>
            <p className="text-xs text-slate-500 truncate">
              {user?.role === 'HR' ? 'HR Manager' : 'Candidate'}
            </p>
          </div>
          <div className="w-2 h-2 rounded-full bg-emerald-500" title="Online" />
        </div>
      </div>
    </aside>
  );
}

